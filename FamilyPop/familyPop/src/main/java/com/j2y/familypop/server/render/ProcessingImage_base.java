package com.j2y.familypop.server.render;

import org.jbox2d.common.Vec2;
import processing.core.PApplet;
import processing.core.PImage;

//++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
//
// FpsBubble
//
//
//++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++

public class ProcessingImage_base {

    protected PImage _image;
    public Vec2 _position = new Vec2();




    //++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
    // 속성
    //
    //++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++

    public Vec2 GetScreenPosition() {
        return _position;
    }

    public Vec2 GetWorldPosition() {
        return _position;
    }

    public void SetPosition(Vec2 pos) { _position = pos;  }





    //++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
    // 초기화
    //
    //++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++

    public ProcessingImage_base(PApplet applet, String imageName)
    {
        _image = applet.loadImage(imageName);
    }


    //++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
    // 렌더링
    //
    //++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++

    //------------------------------------------------------------------------------------------------------------------------------------------------------
    public void Display(PApplet applet)
    {

        if(null == _image)
            return;

        applet.image(_image, _position.x, _position.y);
    }

    public void Display(PApplet applet, int x, int y, int w, int  h)
    {

        if(null == _image)
            return;

        applet.image(_image, x, y, w, h);
    }
}

